public class DoWhileTes {
     
	public static void main(String[] args) {
	      
		int counter = 1; // initialize counter
		
		do {
		     
			System.out.printf("%d ", counter );
			++counter; 
		} // end do...while
		while(counter <=10 );
		
		System.out.println(); // outputs a newline
	} // end main
} // end class DoWhileTes
